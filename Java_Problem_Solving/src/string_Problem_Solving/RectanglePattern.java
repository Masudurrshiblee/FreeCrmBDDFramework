public class RectanglePattern {
    public static void main(String[] args) {
int columns = 5;
        int row = 5;
        for (int i = 1; i <= row; i++) {

            for (int j = 1; j <= columns; j++) {
                System.out.print(" * ");
            }

                System.out.println(" ");
            }
        }
    }
